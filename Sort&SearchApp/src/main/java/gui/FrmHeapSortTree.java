    private void bttnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnSortActionPerformed
        Integer[] orderedList = processAndSortList();
        updateListModel(orderedList);
    }//GEN-LAST:event_bttnSortActionPerformed

    private Integer[] processAndSortList() {
        Integer[] originalList = (Integer[]) modeloList1.toArray();
        int len = originalList.length;
        if (len <= 1) 
            return originalList;

        // Crear un árbol desde el array
            DNodo root = treeConstruct(originalList, 0);

    // Ordenar el árbol
        // Ordenar la lista utilizando un montículo
        HeapSortDLL sorting = new HeapSortDLL();
        return sorting.main_heap(root, len);
    }

    private void updateListModel(Integer[] orderedList) {
        modeloList2.clear();

        for (Integer n : orderedList) {
            modeloList2.addElement(String.valueOf(n));
        }
    }
    
    private DNodo treeConstruct(Integer[] arr, int i) {
        if (i >= arr.length) return null;

        DNodo node = new DNodo(arr[i]);
        node.prev = treeConstruct(arr, 2*i + 1);
        node.next = treeConstruct(arr, 2*i + 2);

        return node; // Retorna la raiz
    }
    
