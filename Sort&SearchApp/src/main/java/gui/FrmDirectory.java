    private void register(){
        String name = txtName.getText();
        int age = Integer.parseInt(txtAge.getText());
        if(age < 14 || age > 99){
            JOptionPane.showMessageDialog(this, "Edad no v√°lida. Ingrese edad mayor a 14 y menor a 99.", "Non-valid age", age);
        }
        char sex = ((String) comboBoxGender.getSelectedItem()).charAt(0);
        
        Person newPerson = new Person(name,age,sex);
        dir.add(newPerson);
        modelList1.addElement(newPerson.toString());
        clearEntry();
        txtName.requestFocus();
    }
