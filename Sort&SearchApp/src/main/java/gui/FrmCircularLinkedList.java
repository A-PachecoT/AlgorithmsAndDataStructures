    private void bttnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnAddActionPerformed
        String text = txtEntryValues.getText();
        if (text != null && !text.isEmpty()) {
            try {
                Integer value = Integer.parseInt(text);
                circularLL.add(value);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El valor ingresado no es v√°lido.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un valor.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }

        txtEntryValues.setText("");
        txtEntryValues.requestFocus();
    }//GEN-LAST:event_bttnAddActionPerformed

    private void bttnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnRemoveActionPerformed
        String text = txtEntryValues.getText();
        if (text != null && !text.isEmpty()) {
            try {
                Integer value = Integer.parseInt(text);
                circularLL.remove(value);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El valor ingresado no es v√°lido.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese un valor.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        txtEntryValues.setText("");
        txtEntryValues.requestFocus();
    }//GEN-LAST:event_bttnRemoveActionPerformed

